import torch
import torch.nn.functional as F
from torch_geometric.nn import GATConv
from torch_geometric.data import Data
import pandas as pd
import numpy as np

print("1. Extracting Physical Features for the AI...")
df = pd.read_csv('mock_police_cdrs_integrated.csv')
df['Timestamp'] = pd.to_datetime(df['Timestamp'])
df['is_night'] = df['Timestamp'].dt.hour.apply(lambda x: 1 if 1 <= x <= 4 else 0)
df['is_high_risk_tower'] = df['Tower_ID'].apply(lambda x: 1 if x in ['TWR_001', 'TWR_002', 'TWR_003'] else 0)

unique_nodes = pd.concat([df['Caller_ID'], df['Receiver_ID']]).unique()
node_mapping = {node: i for i, node in enumerate(unique_nodes)}
num_nodes = len(unique_nodes)

source_nodes = df['Caller_ID'].map(node_mapping).values
target_nodes = df['Receiver_ID'].map(node_mapping).values
edge_index = torch.tensor(np.array([source_nodes, target_nodes]), dtype=torch.long)

feature_list = []
inverse_mapping = {v: k for k, v in node_mapping.items()}
for i in range(num_nodes):
    user_calls = df[(df['Caller_ID'] == inverse_mapping[i]) | (df['Receiver_ID'] == inverse_mapping[i])]
    if len(user_calls) == 0:
        feature_list.append([0.0, 0.0, 0.0])
    else:
        norm_duration = user_calls['Duration_Seconds'].mean() / 3600.0
        night_ratio = user_calls['is_night'].mean()
        tower_ratio = user_calls['is_high_risk_tower'].mean()
        feature_list.append([norm_duration, night_ratio, tower_ratio])

x = torch.tensor(feature_list, dtype=torch.float)

labels = np.zeros(num_nodes)
for orig_id, new_id in node_mapping.items():
    if orig_id in range(1, 21): labels[new_id] = 1
y = torch.tensor(labels, dtype=torch.long)

data = Data(x=x, edge_index=edge_index, y=y)

indices = torch.randperm(num_nodes)
train_size = int(0.8 * num_nodes)
data.train_mask = torch.zeros(num_nodes, dtype=torch.bool)
data.test_mask = torch.zeros(num_nodes, dtype=torch.bool)
data.train_mask[indices[:train_size]] = True
data.test_mask[indices[train_size:]] = True

class SpatioTemporalGAT(torch.nn.Module):
    def __init__(self):
        super(SpatioTemporalGAT, self).__init__()
        self.conv1 = GATConv(in_channels=3, out_channels=8, heads=8, dropout=0.6)
        self.conv2 = GATConv(in_channels=8 * 8, out_channels=2, heads=1, concat=False, dropout=0.6)

    def forward(self, x, edge_index):
        x = F.elu(self.conv1(F.dropout(x, p=0.6, training=self.training), edge_index))
        x = self.conv2(F.dropout(x, p=0.6, training=self.training), edge_index)
        return F.log_softmax(x, dim=1)

model = SpatioTemporalGAT()
optimizer = torch.optim.Adam(model.parameters(), lr=0.01, weight_decay=5e-4)

history_loss, history_acc = [], []

# THE FIX: We add a massive weight to Class 1 (Criminals) so the AI cannot ignore them!
class_weights = torch.tensor([1.0, 50.0])

print("2. Training the Spatio-Temporal Model (with Anomaly Weights)...")
for epoch in range(1, 101):
    model.train()
    optimizer.zero_grad()
    out = model(data.x, data.edge_index)

    # Passing the weights into the loss function
    loss = F.nll_loss(out[data.train_mask], data.y[data.train_mask], weight=class_weights)
    loss.backward()
    optimizer.step()

    model.eval()
    _, pred = model(data.x, data.edge_index).max(dim=1)
    acc = int(pred[data.test_mask].eq(data.y[data.test_mask]).sum()) / int(data.test_mask.sum())

    history_loss.append(loss.item())
    history_acc.append(acc)

    if epoch % 10 == 0:
        print(f'Epoch {epoch:03d} | Loss: {loss:.4f} | Accuracy: {acc*100:.1f}%')

print("SUCCESS! Training Complete.")

